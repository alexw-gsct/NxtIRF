        irf[junc, on = c("seqnames", "start", "end", "strand"), SpliceOverRight := SO_R]
        irf[SpliceOverLeft >= SpliceOverRight, SpliceOverMax := SpliceOverLeft]
        irf[SpliceOverLeft < SpliceOverRight, SpliceOverMax := SpliceOverRight]
        
